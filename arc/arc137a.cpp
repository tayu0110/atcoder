int main(int argc, char* argv[]){
  cin.tie(0);
  ios::sync_with_stdio(0);
  cout << fixed << setprecision(20);
  ll l, r;
  cin >> l >> r;
  ll ans = 1;
  for(ll i=0;i<1000;i++) {
    ll nl = l+i;
    if(nl >= r) break;
    for(ll j=0;j<1000;j++) {
      ll nr = r-j;
      if(nr <= nl) break;
      ll g = gcd(nl, nr);
      if(g == 1) ans = max(ans, nr - nl);
    }
  }
  cout << ans << endl;
  return 0;
}
