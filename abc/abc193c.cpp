int main(int argc,char* argv[]){
  cin.tie(0);
  ios::sync_with_stdio(0);
  cout << fixed << setprecision(20);
  ll n;
  cin >> n;
  set<ll> ck;
  ll ans = n;
  for(ll i = 2; i*i <= n; i++){
    ll now = i*i;
    ll k = i;
    while(now <= n) {
      if(ck.find(now) == ck.end()) {
        // cout << "Now: " << now <<endl;
        ck.insert(now);
        ans--;
      }
      now *= k;
    }
  }
  cout << ans << endl;
  return 0;
}
